package ru.pavelnix;

/**
 * Класс обработки массива
 */
public class RemovingRepetitiveSequencesArray {
    /**
     * Сам массив
     */
    private int[] array;

    /**
     * Конструктор
     *
     * @param array - массив
     */
    public RemovingRepetitiveSequencesArray(int[] array) {
        this.array = array;
    }

    /**
     * Геттер
     *
     * @return - возвращает массив
     */
    public int[] getArray() {
        return array;
    }
}
